package ifForWhile;
import java.util.*;
public class bj10950 {
	static Scanner sc = new Scanner(System.in);
	static int C;

	public static void main(String[] args) {
		C = sc.nextInt();
		
		for(int tc=0;tc<C;tc++) {
			int a = sc.nextInt();
			int b = sc.nextInt();
			
			System.out.println(a+b);
		}
	}

}
